    def _add_labeled_entry(self, parent, row, label, variable, state="normal", width=40):
        tk.Label(parent, text=label, anchor="w").grid(row=row, column=0, sticky="w", pady=4)
        entry = tk.Entry(parent, textvariable=variable, width=width)
        if state != "normal":
            entry.configure(state=state)
        entry.grid(row=row, column=1, sticky="ew", pady=4)
        return entry

    def enqueue_log(self, message: str):
        timestamp = time.strftime("%H:%M:%S")
        self.log_queue.put(f"[{timestamp}] {message}")

    def flush_logs(self):
        while True:
            try:
                line = self.log_queue.get_nowait()
            except queue.Empty:
                break
            self.log_widget.configure(state="normal")
            self.log_widget.insert("end", line + "\n")
            self.log_widget.see("end")
            self.log_widget.configure(state="disabled")
        self.root.after(200, self.flush_logs)

    def on_runtime_state(self, public_url=UNSET, remote_connected=UNSET, remote_info=UNSET):
        def apply_state():
            if public_url is not UNSET:
                self.current_public_url = public_url or ""
                self.tunnel_url_var.set(self.current_public_url)
            if remote_connected is not UNSET:
                self.remote_connected = remote_connected
            if remote_info is not UNSET:
                self.remote_info = remote_info
            if self.remote_connected:
                hostname = self.remote_info.get("hostname", "connected")
                self.remote_state_var.set(f"Remote: connected ({hostname})")
                ssh_user = self.remote_info.get("ssh_user", "notebook")
                self.ssh_hint_var.set(
                    "Run the copied notebook cell first. Then connect from this PC with:\n"
                    f"ssh -p {self.proxy_bind_port_var.get() or DEFAULT_PROXY_PORT} {ssh_user}@127.0.0.1\n"
                    "Password: use the Shared token"
                )
            else:
                self.remote_state_var.set("Remote: disconnected")

        self.root.after(0, apply_state)

    def parse_int(self, raw_value: str, label: str) -> int:
        try:
            return int(raw_value.strip())
        except ValueError as exc:
            raise ValueError(f"{label} must be an integer.") from exc

    def show_error(self, message: str):
        messagebox.showerror("Operation failed", message, parent=self.root)

    def on_install_cloudflared(self, _button):
        def worker():
            try:
                target = download_cloudflared(self.enqueue_log)
                self.root.after(0, lambda: self.cloudflared_var.set(str(target)))
                self.enqueue_log(f"cloudflared downloaded to {target}")
            except Exception as exc:
                self.enqueue_log(f"Failed to download cloudflared: {exc}")
                self.root.after(0, lambda: self.show_error(str(exc)))

        threading.Thread(target=worker, daemon=True).start()

    def on_start_tunnel(self, _button=None):
        try:
            control_port = self.parse_int(self.control_port_var.get(), "Control port")
            token = self.token_var.get().strip()
            if not token:
                raise ValueError("Shared token is required.")
            cloudflared_path = Path(self.cloudflared_var.get().strip())
            if not cloudflared_path.exists():
                raise FileNotFoundError(
                    "cloudflared executable was not found. Download it first or set the correct path."
                )
        except Exception as exc:
            self.show_error(str(exc))
            return

        self.enqueue_log("Starting control server and Cloudflare tunnel.")

        def worker():
            try:
                self.runtime.run_coro(self.runtime.start_server(control_port, token)).result()
                self.runtime.start_cloudflared(cloudflared_path)
            except Exception as exc:
                self.enqueue_log(f"Failed to start tunnel: {exc}")
                self.root.after(0, lambda: self.show_error(str(exc)))

        threading.Thread(target=worker, daemon=True).start()

    def on_stop_tunnel(self, _button=None):
        self.enqueue_log("Stopping tunnel.")

        def worker():
            try:
                self.runtime.stop_cloudflared()
                self.runtime.run_coro(self.runtime.stop_server()).result()
            except Exception as exc:
                self.enqueue_log(f"Failed to stop tunnel cleanly: {exc}")

        threading.Thread(target=worker, daemon=True).start()

    def on_copy_cell_code(self, _button=None):
        self.enqueue_log("Copying notebook cell code with the current tunnel URL.")
