def find_cloudflared() -> Path | None:
    system = platform.system().lower()
    if system == "windows":
        candidates = [
            str(BIN_DIR / "cloudflared.exe"),
            shutil.which("cloudflared"),
            str(BIN_DIR / "cloudflared"),
        ]
    else:
        candidates = [
            str(BIN_DIR / "cloudflared"),
            shutil.which("cloudflared"),
            str(BIN_DIR / "cloudflared.exe"),
        ]
    for candidate in candidates:
        if candidate and Path(candidate).exists():
            return Path(candidate)
    return None


def get_cloudflared_download_info() -> tuple[str, Path]:
    system = platform.system().lower()
    machine = platform.machine().lower()

    if system == "windows":
        filename = "cloudflared-windows-arm64.exe" if "arm" in machine else "cloudflared-windows-amd64.exe"
        target = BIN_DIR / "cloudflared.exe"
    elif system == "linux":
        if any(token in machine for token in ("aarch64", "arm64")):
            filename = "cloudflared-linux-arm64"
        else:
            filename = "cloudflared-linux-amd64"
        target = BIN_DIR / "cloudflared"
    elif system == "darwin":
        if any(token in machine for token in ("arm64", "aarch64")):
            filename = "cloudflared-darwin-arm64.tgz"
        else:
            filename = "cloudflared-darwin-amd64.tgz"
        raise RuntimeError(
            "Automatic cloudflared download is currently implemented for Windows and Linux. "
            f"Please install cloudflared manually for macOS and set its path in the app. Suggested asset: {filename}"
        )
    else:
        raise RuntimeError(f"Unsupported operating system for automatic cloudflared download: {platform.system()}")

    url = f"https://github.com/cloudflare/cloudflared/releases/latest/download/{filename}"
    return url, target


def download_cloudflared(log_callback):
    BIN_DIR.mkdir(parents=True, exist_ok=True)
    url, target = get_cloudflared_download_info()
    log_callback(f"Downloading cloudflared from {url}")
    with urllib.request.urlopen(url) as response, open(target, "wb") as output:
        output.write(response.read())
    if platform.system().lower() != "windows":
        target.chmod(target.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
    return target


class TunnelRuntime:
    def __init__(self, log_callback, state_callback):
        self.log = log_callback
        self.state_callback = state_callback
        self.loop = None
        self.loop_thread = None
        self.runner = None
        self.site = None
        self.control_port = None
        self.token = None
        self.public_url = None
        self.ws = None
        self.cloudflared_process = None
        self.proxy_server = None
        self.proxy_clients = {}
        self.remote_info = {}

    async def _close_writer_quietly(self, writer):
        writer.close()
        try:
            await writer.wait_closed()
        except (ConnectionResetError, BrokenPipeError, OSError):
            pass
        except Exception:
            pass

    def ensure_loop(self):
        if self.loop is not None:
            return
        self.loop = asyncio.new_event_loop()
        self.loop_thread = threading.Thread(target=self._run_loop, daemon=True)
        self.loop_thread.start()

    def _run_loop(self):
        asyncio.set_event_loop(self.loop)
        self.loop.run_forever()

    def run_coro(self, coro):
        self.ensure_loop()
        return asyncio.run_coroutine_threadsafe(coro, self.loop)

    async def start_server(self, control_port: int, token: str):
        if self.runner is not None:
            await self.stop_server()
        self.control_port = control_port
        self.token = token
        app = web.Application()
        app.router.add_get("/healthz", self.handle_health)
        app.router.add_get("/ws", self.handle_ws)
        self.runner = web.AppRunner(app)
        await self.runner.setup()
        self.site = web.TCPSite(self.runner, "127.0.0.1", control_port)
        await self.site.start()
        self.log(f"Control server listening on 127.0.0.1:{control_port}")

    async def stop_server(self):
        if self.proxy_server is not None:
            self.proxy_server.close()
            await self.proxy_server.wait_closed()
            self.proxy_server = None
        for client in list(self.proxy_clients.values()):
            client["writer"].close()
        self.proxy_clients.clear()
        if self.ws is not None:
            await self.ws.close()
            self.ws = None
        if self.site is not None:
            await self.site.stop()
            self.site = None
        if self.runner is not None:
            await self.runner.cleanup()
            self.runner = None

    async def handle_health(self, request):
        return web.json_response(
            {
                "ok": True,
                "connected": self.ws is not None,
                "public_url": self.public_url,
                "remote_info": self.remote_info,
            }
        )

    async def handle_ws(self, request):
        provided_token = request.query.get("token") or request.headers.get("x-kaggle-tunnel-token")
        if provided_token != self.token:
            return web.Response(status=403, text="invalid token")
        ws = web.WebSocketResponse(max_msg_size=0)
        await ws.prepare(request)
        if self.ws is not None:
            await self.ws.close(code=4000, message=b"replaced")
        self.ws = ws
        self.state_callback(remote_connected=True)
        self.log("Remote notebook connected.")
        async for message in ws:
            if message.type == WSMsgType.TEXT:
                await self.process_remote_message(json.loads(message.data))
            elif message.type == WSMsgType.ERROR:
                self.log(f"Websocket error: {ws.exception()}")
        self.ws = None
        self.remote_info = {}
        self.state_callback(remote_connected=False, remote_info={})
        self.log("Remote notebook disconnected.")
        return ws

    async def process_remote_message(self, payload):
        message_type = payload.get("type")
        if message_type == "hello":
            self.remote_info = payload
            self.state_callback(remote_connected=True, remote_info=payload)
            self.log(
                f"Notebook hello from {payload.get('hostname', 'unknown')} "
                f"({payload.get('platform', 'unknown')})"
            )
            if payload.get("ssh_port"):
                self.log(
                    "Embedded SSH ready on notebook side: "
                    f"{payload.get('ssh_host', '127.0.0.1')}:{payload.get('ssh_port')} "
                    f"user={payload.get('ssh_user', 'notebook')}"
                )
        elif message_type == "command_result":
            request_id = payload.get("request_id")
            self.log(
                f"[command {request_id}] rc={payload.get('returncode')}\n"
                f"{payload.get('stdout', '')}{payload.get('stderr', '')}"
            )
        elif message_type == "tcp_data":
            client = self.proxy_clients.get(payload["connection_id"])
            if client:
                client["writer"].write(base64.b64decode(payload["data"]))
                await client["writer"].drain()
        elif message_type == "tcp_opened":
            self.log(f"Remote proxy opened: {payload['connection_id']}")
        elif message_type == "tcp_closed":
            connection_id = payload["connection_id"]
            client = self.proxy_clients.pop(connection_id, None)
            if client:
                await self._close_writer_quietly(client["writer"])
            self.log(f"Remote proxy closed: {connection_id}")
            if "error" in payload:
                self.log(f"Remote proxy error for {connection_id}: {payload['error']}")
        elif message_type == "ssh_status":
            self.log(f"SSH tunnel status: {json.dumps(payload, indent=2)}")
        elif message_type == "log":
            self.log(payload.get("message", "remote log"))

    async def send(self, payload):
        if self.ws is None:
            raise RuntimeError("Remote notebook is not connected.")
        await self.ws.send_json(payload)

    def start_cloudflared(self, cloudflared_path: Path):
        if self.control_port is None:
            raise RuntimeError("Start the control server first.")
        if self.cloudflared_process and self.cloudflared_process.poll() is None:
            raise RuntimeError("cloudflared is already running.")
        command = [
            str(cloudflared_path),
            "tunnel",
            "--url",
            f"http://127.0.0.1:{self.control_port}",
            "--no-autoupdate",
        ]
        self.log(f"Starting cloudflared: {' '.join(command)}")
        self.cloudflared_process = subprocess.Popen(
            command,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )
        threading.Thread(target=self._watch_cloudflared_output, daemon=True).start()

    def _watch_cloudflared_output(self):
        assert self.cloudflared_process is not None
        for line in self.cloudflared_process.stdout:
            line = line.rstrip()
            self.log(f"[cloudflared] {line}")
            if "trycloudflare.com" in line and self.public_url is None:
                for part in line.split():
                    if part.startswith("https://") and "trycloudflare.com" in part:
                        self.public_url = part
                        self.state_callback(public_url=self.public_url)
                        self.log(f"Tunnel URL ready: {self.public_url}")
                        break
        return_code = self.cloudflared_process.wait()
        self.log(f"cloudflared exited with code {return_code}")
        self.public_url = None
        self.state_callback(public_url=None)

    def stop_cloudflared(self):
        if self.cloudflared_process and self.cloudflared_process.poll() is None:
            self.cloudflared_process.terminate()
            try:
                self.cloudflared_process.wait(timeout=10)
            except subprocess.TimeoutExpired:
                self.cloudflared_process.kill()
        self.cloudflared_process = None
        self.public_url = None
        self.state_callback(public_url=None)

    async def start_proxy(self, bind_port: int, target_host: str, target_port: int):
        if self.proxy_server is not None:
            self.proxy_server.close()
            await self.proxy_server.wait_closed()
        self.proxy_server = await asyncio.start_server(
            lambda reader, writer: self.handle_proxy_client(reader, writer, target_host, target_port),
            "127.0.0.1",
            bind_port,
        )
        self.log(f"Local proxy listening on 127.0.0.1:{bind_port} -> {target_host}:{target_port}")

    async def stop_proxy(self):
        if self.proxy_server is not None:
            self.proxy_server.close()
            await self.proxy_server.wait_closed()
            self.proxy_server = None
            self.log("Local proxy stopped.")

    async def handle_proxy_client(self, reader, writer, target_host: str, target_port: int):
        connection_id = secrets.token_hex(8)
        self.proxy_clients[connection_id] = {"writer": writer}
        await self.send(
            {
                "type": "tcp_open",
                "connection_id": connection_id,
                "host": target_host,
                "port": target_port,
            }
        )
        self.log(f"Accepted local proxy client {connection_id}")
        try:
            while True:
                chunk = await reader.read(65536)
                if not chunk:
                    break
                await self.send(
                    {
                        "type": "tcp_data",
                        "connection_id": connection_id,
                        "data": base64.b64encode(chunk).decode("ascii"),
                    }
                )
        except (ConnectionResetError, BrokenPipeError, OSError) as exc:
            self.log(f"Local proxy client {connection_id} disconnected: {exc}")
        finally:
            try:
                await self.send({"type": "tcp_close", "connection_id": connection_id})
            except Exception:
                pass
            self.proxy_clients.pop(connection_id, None)
            await self._close_writer_quietly(writer)

    def generate_notebook(self):
        if not self.public_url:
            raise RuntimeError("Tunnel URL is not ready yet.")
        GENERATED_DIR.mkdir(parents=True, exist_ok=True)
        control_url = self.public_url.replace("https://", "wss://").rstrip("/") + f"/ws?token={self.token}"
        notebook_code = NOTEBOOK_TEMPLATE.format(
            control_url=control_url,
            control_token=self.token,
        )
        py_target = GENERATED_DIR / "remote_notebook_cell.py"
        ipynb_target = GENERATED_DIR / "remote_notebook.ipynb"
        py_target.write_text(notebook_code, encoding="utf-8")
        notebook_payload = {
            "cells": [
                {
                    "cell_type": "code",
                    "execution_count": None,
                    "metadata": {},
                    "outputs": [],
                    "source": notebook_code.splitlines(keepends=True),
                }
            ],
            "metadata": {
                "kernelspec": {
                    "display_name": "Python 3",
                    "language": "python",
                    "name": "python3",
                },
                "language_info": {
                    "name": "python",
                },
            },
            "nbformat": 4,
            "nbformat_minor": 5,
        }
        ipynb_target.write_text(json.dumps(notebook_payload, indent=2), encoding="utf-8")
        return py_target, ipynb_target, notebook_code

    def build_notebook_cell_code(self):
        if not self.public_url:
            raise RuntimeError("Tunnel URL is not ready yet.")
        control_url = self.public_url.replace("https://", "wss://").rstrip("/") + f"/ws?token={self.token}"
        return NOTEBOOK_TEMPLATE.format(
            control_url=control_url,
            control_token=self.token,
        )

    def build_agent_prompt(self, local_bind_port: int, remote_host: str, remote_port: int):
        if not self.public_url:
            raise RuntimeError("Tunnel URL is not ready yet.")
        token = self.token or ""
